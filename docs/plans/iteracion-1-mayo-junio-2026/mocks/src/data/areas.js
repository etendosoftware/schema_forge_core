export function taskIsVisible(task, selectedStage) {
  return stageOrder.indexOf(task[1]) <= stageOrder.indexOf(selectedStage);
}

export function areaIsVisible(area, selectedStage) {
  return stageOrder.indexOf(area.start) <= stageOrder.indexOf(selectedStage);
}
