#!/usr/bin/env node

/**
 * generate-report-template.js — Generate a Handlebars template from a report-contract.json.
 *
 * Supports:
 *   - listing: flat table
 *   - grouped-listing: hierarchical group headers + detail rows
 *
 * Usage:
 *   node cli/src/generate-report-template.js --contract <path-to-contract.json>
 *   node cli/src/generate-report-template.js --contract <path> --output <dir>
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { existsSync } from 'node:fs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, '..', '..');

// ---------------------------------------------------------------------------
// Helpers code for jsreport (group-break detection + formatting)
// ---------------------------------------------------------------------------

function generateHelpersCode(contract) {
  const groupFields = (contract.groups || []).map(g => g.field);

  return `
// Group-break detection: tracks previous values per group field
var _prevGroupValues = {};

function isGroupBreak(field, currentValue) {
  var prev = _prevGroupValues[field];
  _prevGroupValues[field] = currentValue;
  return prev !== currentValue;
}

function resetGroupTracking() {
  _prevGroupValues = {};
}

// Format helpers
function formatDate(value) {
  if (value == null || value === '') return '';
  var d = new Date(value);
  if (isNaN(d.getTime())) return String(value);
  return new Intl.DateTimeFormat('en-GB', { day: '2-digit', month: '2-digit', year: 'numeric' }).format(d);
}

function formatCurrency(value) {
  if (value == null) return '';
  var num = Number(value);
  if (isNaN(num)) return String(value);
  return new Intl.NumberFormat('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(num);
}

function formatBoolean(value) {
  return value ? 'Yes' : 'No';
}

function formatNumber(value) {
  if (value == null) return '';
  var num = Number(value);
  if (isNaN(num)) return String(value);
  return new Intl.NumberFormat('en-US').format(num);
}

function ifCond(v1, operator, v2, options) {
  switch (operator) {
    case '===': return v1 === v2 ? options.fn(this) : options.inverse(this);
    case '!==': return v1 !== v2 ? options.fn(this) : options.inverse(this);
    default: return options.inverse(this);
  }
}

function eq(a, b) { return a === b; }
`.trim();
}

// ---------------------------------------------------------------------------
// Template generators
// ---------------------------------------------------------------------------

function indent(level) {
  return '  '.repeat(level);
}

function cellContent(col, rowExpr) {
  const val = `(lookup ${rowExpr} '${col.field}')`;
  switch (col.type) {
    case 'date':
    case 'datetime':
      return `{{formatDate ${val}}}`;
    case 'amount':
      return `{{formatCurrency ${val}}}`;
    case 'number':
      return `{{formatNumber ${val}}}`;
    case 'boolean':
      return `{{formatBoolean ${val}}}`;
    default:
      return `{{lookup ${rowExpr} '${col.field}'}}`;
  }
}

function cellClass(col) {
  if (col.type === 'amount' || col.type === 'number') return ' class="cell-amount"';
  if (col.type === 'boolean') return ' class="cell-boolean"';
  return '';
}

/**
 * Generate a flat listing template (no groups).
 */
function generateListingTemplate(contract) {
  const cols = contract.columns || [];
  return `{{!-- Auto-generated listing template for: ${contract.reportId} --}}
<!DOCTYPE html>
<html><head><meta charset="UTF-8"><style>{{{css}}}</style></head>
<body><div class="report-container">
  <div class="report-header">
    <div><div class="report-title">{{meta.title}}</div></div>
    <div class="report-meta"><div>{{formatDate meta.generatedAt}}</div><div>{{meta.recordCount}} records</div></div>
  </div>

  {{#if meta.filters.length}}
  <div class="report-filters"><strong>Filters:</strong>
    {{#each meta.filters}}<span>{{this.label}}: {{this.value}} &nbsp;</span>{{/each}}
  </div>
  {{/if}}

  <table class="report-table">
    <thead><tr>
${cols.map(c => `      <th style="width: ${c.width || 'auto'}">${c.label.en_US}</th>`).join('\n')}
    </tr></thead>
    <tbody>
      {{#each rows}}<tr>
${cols.map(c => `        <td${cellClass(c)}>${cellContent(c, 'this')}</td>`).join('\n')}
      </tr>{{/each}}
    </tbody>
  </table>

  <div class="report-footer">
    <div class="report-summary">Total: {{meta.recordCount}} records</div>
    <div>Generated by Etendo Go</div>
  </div>
</div></body></html>`;
}

/**
 * Generate a grouped listing template with hierarchical group headers.
 */
function generateGroupedTemplate(contract) {
  const cols = contract.columns || [];
  const groups = contract.groups || [];

  // Group header colors (progressively lighter)
  const groupColors = [
    { bg: '#1a1a2e', fg: '#ffffff' },
    { bg: '#334155', fg: '#ffffff' },
    { bg: '#64748b', fg: '#ffffff' },
    { bg: '#94a3b8', fg: '#1e293b' },
  ];

  // Build group header HTML for a single group level
  function groupHeaderHtml(group, level) {
    const color = groupColors[Math.min(level, groupColors.length - 1)];
    const headerFields = group.headerFields || [];

    let subHeaderHtml = '';
    if (headerFields.length > 0) {
      const cells = headerFields.map(f => {
        const label = typeof f.label === 'object' ? f.label.en_US : f.label;
        const val = f.type === 'date' ? `{{formatDate (lookup this '${f.field}')}}` : `{{lookup this '${f.field}'}}`;
        return `<span style="margin-right: 3mm;"><strong>${label}:</strong> ${val}</span>`;
      }).join('\n              ');

      subHeaderHtml = `
        <tr class="group-subheader">
          <td colspan="${cols.length}" style="background: #f1f5f9; padding: 1.5mm 4mm; font-size: 8pt; color: #475569;">
            ${cells}
          </td>
        </tr>`;
    }

    return `        {{#if (isGroupBreak '${group.field}' (lookup this '${group.field}'))}}
        <tr class="group-header group-level-${level}">
          <td colspan="${cols.length}" style="background: ${color.bg}; color: ${color.fg}; font-weight: 600; padding: 2mm 4mm; font-size: ${11 - level}pt;">
            ${group.label.en_US}: {{lookup this '${group.field}'}}
          </td>
        </tr>${subHeaderHtml}
        {{/if}}`;
  }

  const groupHeaders = groups.map((g, i) => groupHeaderHtml(g, i)).join('\n');

  return `{{!-- Auto-generated grouped listing template for: ${contract.reportId} --}}
<!DOCTYPE html>
<html><head><meta charset="UTF-8">
<style>
{{{css}}}
.group-header td { border: none !important; }
.group-subheader td { border-bottom: 1px solid #e2e8f0 !important; }
</style>
</head>
<body><div class="report-container">
  <div class="report-header">
    <div><div class="report-title">{{meta.title}}</div></div>
    <div class="report-meta"><div>{{formatDate meta.generatedAt}}</div><div>{{meta.recordCount}} records</div></div>
  </div>

  {{#if meta.filters.length}}
  <div class="report-filters"><strong>Filters:</strong>
    {{#each meta.filters}}<span>{{this.label}}: {{this.value}} &nbsp;</span>{{/each}}
  </div>
  {{/if}}

  <table class="report-table">
    <thead><tr>
${cols.map(c => `      <th style="width: ${c.width || 'auto'}">${c.label.en_US}</th>`).join('\n')}
    </tr></thead>
    <tbody>
      {{#each rows}}
${groupHeaders}
        <tr>
${cols.map(c => `          <td${cellClass(c)}>${cellContent(c, 'this')}</td>`).join('\n')}
        </tr>
      {{/each}}
    </tbody>
  </table>

  <div class="report-footer">
    <div class="report-summary">Total: {{meta.recordCount}} records</div>
    <div>Generated by Etendo Go</div>
  </div>
</div></body></html>`;
}

// ---------------------------------------------------------------------------
// Contract enrichment — detect group header sub-fields from Jasper metadata
// ---------------------------------------------------------------------------

/**
 * Enrich the contract with group header fields by analyzing which fields
 * belong to each group level (fields that are not detail columns or group keys).
 */
function enrichContractWithGroupHeaderFields(contract) {
  if (!contract.groups || contract.groups.length === 0) return contract;

  // All fields from the jasper source
  const allFields = contract._allFields || [];
  const detailFieldNames = new Set((contract.columns || []).map(c => c.field));
  const groupFieldNames = new Set(contract.groups.map(g => g.field));

  // Fields that are neither detail columns nor group keys are potential header fields
  const headerCandidates = allFields.filter(f =>
    !detailFieldNames.has(f.name) && !groupFieldNames.has(f.name)
  );

  // Assign header candidates to the last (most detailed) group
  if (headerCandidates.length > 0 && contract.groups.length > 0) {
    const lastGroup = contract.groups[contract.groups.length - 1];
    contract._groupHeaderFields = contract._groupHeaderFields || {};
    contract._groupHeaderFields[lastGroup.field] = headerCandidates.map(f => ({
      field: f.name,
      label: f.name.replace(/([A-Z])/g, ' $1').replace(/_/g, ' ').replace(/\b\w/g, c => c.toUpperCase()).trim(),
      type: f.type || 'string',
    }));
  }

  return contract;
}

// ---------------------------------------------------------------------------
// Main generation function
// ---------------------------------------------------------------------------

export function generateTemplate(contract) {
  const enriched = enrichContractWithGroupHeaderFields(contract);

  if (enriched.type === 'grouped-listing' && enriched.groups?.length > 0) {
    return {
      template: generateGroupedTemplate(enriched),
      helpers: generateHelpersCode(enriched),
    };
  }

  return {
    template: generateListingTemplate(enriched),
    helpers: generateHelpersCode(enriched),
  };
}

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

function parseArgs(argv) {
  const opts = { contract: null, output: null };
  let i = 0;
  while (i < argv.length) {
    if (argv[i] === '--contract' && argv[i + 1]) {
      opts.contract = argv[i + 1];
      i += 2;
    } else if (argv[i] === '--output' && argv[i + 1]) {
      opts.output = argv[i + 1];
      i += 2;
    } else {
      i += 1;
    }
  }
  return opts;
}

const isMain = process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];

if (isMain) {
  const opts = parseArgs(process.argv.slice(2));

  if (!opts.contract) {
    console.error('Usage: generate-report-template.js --contract <path-to-contract.json> [--output <dir>]');
    process.exit(1);
  }

  const raw = await readFile(opts.contract, 'utf8');
  const contract = JSON.parse(raw);

  // If we have jasper source info, try to load the parsed fields for enrichment
  if (contract.jasper?.originalFile) {
    try {
      const { parseJrxml } = await import('./extract-from-jasper.js');
      const jrxmlPath = join(ROOT, contract.jasper.originalFile);
      if (existsSync(jrxmlPath)) {
        const xml = await readFile(jrxmlPath, 'utf8');
        const parsed = parseJrxml(xml);
        contract._allFields = parsed.fields;
      }
    } catch {
      // Best effort — skip if extract-from-jasper not available
    }
  }

  const { template, helpers } = generateTemplate(contract);

  // Output directory
  const contractDir = dirname(opts.contract);
  const outputDir = opts.output || contractDir;
  if (!existsSync(outputDir)) {
    await mkdir(outputDir, { recursive: true });
  }

  const templatePath = join(outputDir, 'template.hbs');
  const helpersPath = join(outputDir, 'helpers.js');

  await writeFile(templatePath, template + '\n');
  await writeFile(helpersPath, helpers + '\n');

  console.log(`[generate-report-template] Type: ${contract.type}`);
  console.log(`  Groups: ${contract.groups?.length || 0}`);
  console.log(`  Columns: ${contract.columns?.length || 0}`);
  console.log(`  Template: ${templatePath}`);
  console.log(`  Helpers:  ${helpersPath}`);
}
